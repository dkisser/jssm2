	package org.lf.jssm.db.pojo;

public class CYhzw {
	private String dm;
	private String mc;
	private String pym;
	private String tybz;
	private String remark;
	
	


	public CYhzw(String dm, String mc, String pym, String tybz, String remark) {
		super();
		this.dm = dm;
		this.mc = mc;
		this.pym = pym;
		this.tybz = tybz;
		this.remark = remark;
	}
	public String getDm() {
		return dm;
	}
	public void setDm(String dm) {
		this.dm = dm;
	}
	public String getMc() {
		return mc;
	}
	public void setMc(String mc) {
		this.mc = mc;
	}
	public String getPym() {
		return pym;
	}
	public void setPym(String pym) {
		this.pym = pym;
	}
	public String getTybz() {
		return tybz;
	}
	public void setTybz(String tybz) {
		this.tybz = tybz;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	

}
