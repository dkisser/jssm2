package org.lf.jssm.service.model;
/**
 * 封装年月查询扫描的次数
 * @author Administrator
 */
public class DayInfo {
	private int day;
	private int count;
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}
